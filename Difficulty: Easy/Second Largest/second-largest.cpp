class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
         int m = -1 , sm = -1;
        for(int a : arr){
            if(a > m){
                sm = m;
                m = a;
            }else if(a < m && a > sm){
                sm = a;
            }
        }
        return sm;
    }
    
    
};



